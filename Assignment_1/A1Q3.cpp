#include <iostream>
using namespace std;

int main()
{
    float a, b;
    cout << "Enter value of 1st number: ";
    cin >> a;
    cout << "Enter value of second number: ";
    cin >> b;

    cout << "Addition: " << a + b << endl;
    cout << "Subtraction: " << a - b << endl;
    cout << "Multiplication: " << a * b << endl;
    cout << "Division: " << a / b << endl;

    return 0;
}

